#!/usr/bin/env python

def add(a, b):
    """
    Add two numbers and return the result.
    :param a: The first number.
    :param b: The second number.
    :return: The sum of a and b.
    """
    return a + b

# Calling the add function and capturing the return value
result = add(3, 5)
print(f"The sum is {result}")